export function getFieldLabel(key, lang) {
  return fieldLabels[lang]?.[key] || fieldLabels.en[key] || key
}

export function getFieldNormal(key, lang) {
  return fieldNormals[lang]?.[key] || fieldNormals.en[key] || null
}

export function getFeatureInfo(key, lang) {
  const info = featureInfo[key]
  if (!info) return null
  const localized = info[lang] || info.en
  return { label: getFieldLabel(key, lang), ...localized }
}

export function localizeOptionLabel(opt, fieldKey, lang) {
  if (typeof opt.label === 'string') {
    const map = {
      Male: optionLabels.male, Female: optionLabels.female,
      No: optionLabels.no, Yes: optionLabels.yes,
      'No (< 120 mg/dL)': optionLabels.fbsNo, 'Yes (> 120 mg/dL)': optionLabels.fbsYes,
      'Class 0 – Normal': optionLabels.ecg0, 'Class 1 – ST Abnormality': optionLabels.ecg1, 'Class 2 – LV Hypertrophy': optionLabels.ecg2,
      'Slope 0 – Downsloping': optionLabels.slope0, 'Slope 1 – Flat': optionLabels.slope1, 'Slope 2 – Upsloping': optionLabels.slope2,
      'Value 0': optionLabels.thal0, 'Value 1 – Fixed Defect': optionLabels.thal1, 'Value 2 – Normal': optionLabels.thal2, 'Value 3 – Reversible Defect': optionLabels.thal3,
    }
    if (map[opt.label]) return map[opt.label][lang]
    if (opt.label.startsWith('Type ')) return optionLabels.type[lang](opt.value)
    if (/^\d+ vessel/.test(opt.label)) return optionLabels.vessel[lang](opt.value)
  }
  return opt.label
}

export function localizeField(field, lang, t) {
  const unitMap = { count: t.unitCount, score: t.unitScore, years: t.unitYears }
  const localized = {
    ...field,
    label: getFieldLabel(field.key, lang),
    normal: field.normal ? (getFieldNormal(field.key, lang) || field.normal) : field.normal,
    unit: field.unit ? (unitMap[field.unit] || field.unit) : field.unit,
  }
  if (field.options) {
    localized.options = field.options.map((opt) => ({
      ...opt,
      label: localizeOptionLabel(opt, field.key, lang),
    }))
  }
  return localized
}

export function localizeFields(fields, lang, t) {
  return fields.map((f) => localizeField(f, lang, t))
}

export function getModelName(name, t) {
  const map = {
    'Logistic Regression': t.modelLogistic,
    KNN: t.modelKnn,
    'Decision Tree': t.modelTree,
  }
  return map[name] || name
}

export function translateDiseaseName(disease, t) {
  if (disease === 'Diabetes') return t.diabetesLabel
  if (disease === 'Heart Disease') return t.heartDiseaseLabel
  return disease
}

export function translateResultLabel(prediction, disease, t) {
  const isDiabetes = disease === 'Diabetes'
  if (prediction === 1) return isDiabetes ? t.hasDiabetes : t.hasHeartDisease
  return isDiabetes ? t.noDiabetes : t.noHeartDisease
}

export function translateExplanation(prediction, disease, t) {
  const isDiabetes = disease === 'Diabetes'
  if (prediction === 1) return isDiabetes ? t.explainDiabetesHigh : t.explainHeartHigh
  return isDiabetes ? t.explainDiabetesLow : t.explainHeartLow
}

export function localizePrediction(data, disease, t) {
  if (!data) return null
  return {
    ...data,
    best_model: {
      ...data.best_model,
      nameKey: data.best_model.name,
      name: getModelName(data.best_model.name, t),
      label: translateResultLabel(data.best_model.prediction, disease, t),
      explanation: translateExplanation(data.best_model.prediction, disease, t),
    },
    models: data.models.map((m) => ({
      ...m,
      nameKey: m.name,
      name: getModelName(m.name, t),
      label: translateResultLabel(m.prediction, disease, t),
    })),
  }
}

