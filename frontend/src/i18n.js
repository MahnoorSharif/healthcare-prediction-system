export const LANG_KEY = 'hc_lang'

export function loadLang() {
  try {
    const saved = localStorage.getItem(LANG_KEY)
    return saved === 'ur' ? 'ur' : 'en'
  } catch {
    return 'en'
  }
}

export function saveLang(lang) {
  try { localStorage.setItem(LANG_KEY, lang) } catch {}
}
